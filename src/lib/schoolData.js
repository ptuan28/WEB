export const ALL_SCHOOLS = Object.keys(SCHOOL_MAJOR_MAP).sort();

// Lấy ngành theo trường
export function getMajorsBySchool(school) {
  if (!school) return getAllMajors();
  return SCHOOL_MAJOR_MAP[school] || [];
}

// Lấy tất cả ngành (không trùng)
export function getAllMajors() {
  const all = new Set();
  Object.values(SCHOOL_MAJOR_MAP).forEach(majors => majors.forEach(m => all.add(m)));
  return [...all].sort();
}

